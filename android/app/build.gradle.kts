// Release signing is configured only when keystore.properties exists (it is
// gitignored, so secrets never enter version control). Without it, release
// builds stay unsigned so CI / contributors can still build debug.
val keystorePropsFile = rootProject.file("keystore.properties")
val keystoreProps = Properties().apply {
    if (keystorePropsFile.exists()) keystorePropsFile.inputStream().use { load(it) }
}

